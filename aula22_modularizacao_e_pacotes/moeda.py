
def aumentar(n, valor=0):
    soma = n + valor
    return soma
    
    
def diminuir(valor, n=0):
    subtracao = valor - n
    return subtracao
    
    
def dobro(valor):
    dobro = valor * 2
    return dobro
    
    
def metade(valor):
    metade = valor / 2
    return metade

def moedas(valor):
    res = f'R${valor}'
    return res
